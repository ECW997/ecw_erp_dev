    public function Companyedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_company');
        $this->db->where('idtbl_company', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_company;
        $obj->company_name=$respond->row(0)->company;
        $obj->code=$respond->row(0)->code;
        $obj->address=$respond->row(0)->address1;
        $obj->mobile_no=$respond->row(0)->mobile;
        $obj->email=$respond->row(0)->email;
        

        echo json_encode($obj);
    }
