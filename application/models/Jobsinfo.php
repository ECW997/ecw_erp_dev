    public function Jobsedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_jobs');
        $this->db->where('idtbl_jobs', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_jobs;
        $obj->job_name=$respond->row(0)->job_name;
        $obj->job_type=$respond->row(0)->job_type;

        echo json_encode($obj);
    }

