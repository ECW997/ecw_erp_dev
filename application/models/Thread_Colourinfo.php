    public function Thread_Colouredit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_thread_colour');
        $this->db->where('idtbl_thread_colour', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_thread_colour;
        $obj->code=$respond->row(0)->thread_colour_code;
        $obj->colour=$respond->row(0)->thread_colour;

        echo json_encode($obj);
    }
