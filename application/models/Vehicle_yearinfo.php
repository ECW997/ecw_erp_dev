    public function Vehicle_yearedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_vehicle_year');
        $this->db->where('idtbl_vehicle_year', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_vehicle_year;
        $obj->year_name=$respond->row(0)->year_name;
        

        echo json_encode($obj);
    }
