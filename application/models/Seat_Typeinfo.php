    public function Seat_Typeedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_seat_type');
        $this->db->where('idtbl_seat_type', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_seat_type;
        $obj->seat_type=$respond->row(0)->seat_type;
        $obj->seat_category=$respond->row(0)->seat_category;

        echo json_encode($obj);
    }
